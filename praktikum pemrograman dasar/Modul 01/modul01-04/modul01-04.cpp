/* manipulasi variabel bertipe float */

#include <stdio.h>
#include <conio.h>

main()
{
	float nilai=-849.245e+2;
	printf("isi variabel nilai format standar       = %f\n", nilai);
	printf("isi variabel nilai format eksponensial  = %e\n", nilai);
	printf("isi variabel nilai format g adalah      = %g\n", nilai);
	printf("isi variabel nilai format 10.1 adalah   = %10.1f\n", nilai);
	getchar();
	return 0;
}
